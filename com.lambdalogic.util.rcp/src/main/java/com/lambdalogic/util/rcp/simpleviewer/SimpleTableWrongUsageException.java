package com.lambdalogic.util.rcp.simpleviewer;

public class SimpleTableWrongUsageException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public SimpleTableWrongUsageException() {
		super();
	}

	public SimpleTableWrongUsageException(String message, Throwable cause) {
		super(message, cause);
	}

	public SimpleTableWrongUsageException(String message) {
		super(message);
	}

	public SimpleTableWrongUsageException(Throwable cause) {
		super(cause);
	}

}
